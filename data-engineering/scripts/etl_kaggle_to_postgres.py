def transform_data(df: pd.DataFrame, table_name: str) -> pd.DataFrame:
    """Clean and transform the dataframe."""
    logger.info(f"🔄 TRANSFORM: Processing {len(df)} rows × {len(df.columns)} columns...")
    start = time.time()

    original_shape = df.shape

    # 1. Normalize column names (lowercase, underscores, no special chars)
    df.columns = [
        re.sub(r"[^a-z0-9_]", "", col.lower().replace(" ", "_").replace("-", "_"))
        for col in df.columns
    ]
    # Remove leading/trailing underscores and double underscores
    df.columns = [re.sub(r"_+", "_", col).strip("_") for col in df.columns]
    logger.info(f"   ✅ Normalized {len(df.columns)} column names")

    # 2. Strip whitespace from string columns
    str_cols = df.select_dtypes(include=["object"]).columns
    for col in str_cols:
        df[col] = df[col].astype(str).str.strip()
        df[col] = df[col].replace({"nan": None, "None": None, "": None})
    logger.info(f"   ✅ Stripped whitespace from {len(str_cols)} string columns")

    # 3. Auto-detect and parse date columns
    date_cols_detected = []
    for col in str_cols:
        sample = df[col].dropna().head(100)
        if sample.empty:
            continue
        try:
            parsed = pd.to_datetime(sample, infer_datetime_format=True)
            if parsed.notna().sum() > len(sample) * 0.8:
                df[col] = pd.to_datetime(df[col], errors="coerce")
                date_cols_detected.append(col)
        except (ValueError, TypeError):
            pass
    if date_cols_detected:
        logger.info(f"   ✅ Auto-detected date columns: {date_cols_detected}")

    # 4. Optimize numeric types
    for col in df.select_dtypes(include=["float64"]).columns:
        if df[col].dropna().apply(lambda x: x == int(x) if pd.notna(x) else True).all():
            df[col] = df[col].astype("Int64")  # nullable integer
    logger.info(f"   ✅ Optimized numeric types")

    # 5. Remove completely empty rows
    before = len(df)
    df = df.dropna(how="all")
    dropped = before - len(df)
    if dropped:
        logger.info(f"   ✅ Dropped {dropped} completely empty rows")

    # 6. Remove exact duplicate rows
    before = len(df)
    df = df.drop_duplicates()
    deduped = before - len(df)
    if deduped:
        logger.info(f"   ✅ Removed {deduped} duplicate rows")

    elapsed = time.time() - start
    logger.info(
        f"   📊 Transform complete: {original_shape} → {df.shape} "
        f"({elapsed:.2f}s)"
    )

    return df


