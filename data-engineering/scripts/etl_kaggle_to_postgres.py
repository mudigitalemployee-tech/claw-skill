def extract_from_kaggle(dataset_slug: str) -> str:
    """Download dataset from Kaggle and return path to CSV."""
    logger.info(f"📥 EXTRACT: Downloading dataset '{dataset_slug}' from Kaggle...")

    # If it's a local file, skip download
    if os.path.isfile(dataset_slug):
        logger.info(f"📂 Using local file: {dataset_slug}")
        return dataset_slug

    os.makedirs(DATA_DIR, exist_ok=True)

    # Download via kaggle CLI
    cmd = f"kaggle datasets download -d {dataset_slug} -p {DATA_DIR} --unzip --force"
    logger.info(f"   Running: {cmd}")
    exit_code = os.system(cmd)

    if exit_code != 0:
        raise RuntimeError(f"Kaggle download failed with exit code {exit_code}")

    # Find the downloaded CSV(s)
    csv_files = glob.glob(os.path.join(DATA_DIR, "**/*.csv"), recursive=True)
    if not csv_files:
        # Check for zip files and extract
        zip_files = glob.glob(os.path.join(DATA_DIR, "*.zip"))
        for zf in zip_files:
            with zipfile.ZipFile(zf, "r") as z:
                z.extractall(DATA_DIR)
        csv_files = glob.glob(os.path.join(DATA_DIR, "**/*.csv"), recursive=True)

    if not csv_files:
        raise FileNotFoundError(f"No CSV files found after downloading {dataset_slug}")

    # Use the largest CSV if multiple
    csv_files.sort(key=os.path.getsize, reverse=True)
    selected = csv_files[0]
    logger.info(f"   Found {len(csv_files)} CSV file(s). Using: {selected}")
    logger.info(f"   Size: {os.path.getsize(selected) / 1024 / 1024:.2f} MB")

    return selected


