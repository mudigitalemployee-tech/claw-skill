def ensure_database(host, port, user, password, dbname):
    """Create the database if it doesn't exist."""
    try:
        conn = psycopg2.connect(host=host, port=port, user=user, password=password, dbname="postgres")
        conn.set_isolation_level(ISOLATION_LEVEL_AUTOCOMMIT)
        cur = conn.cursor()
        cur.execute(f"SELECT 1 FROM pg_database WHERE datname = %s", (dbname,))
        if not cur.fetchone():
            cur.execute(f'CREATE DATABASE "{dbname}"')
            logger.info(f"   ✅ Created database: {dbname}")
        else:
            logger.info(f"   ✅ Database exists: {dbname}")
        cur.close()
        conn.close()
    except psycopg2.OperationalError as e:
        logger.warning(f"   ⚠️ Could not check/create database: {e}")
        logger.info(f"   Attempting to connect directly to {dbname}...")


def load_to_postgres(
    df: pd.DataFrame,
    table_name: str,
    host=DEFAULT_HOST,
    port=DEFAULT_PORT,
    user=DEFAULT_USER,
    password=DEFAULT_PASSWORD,
    database=DEFAULT_DB,
    if_exists="replace",
    batch_size=5000,
):
    """Load dataframe into PostgreSQL."""
    logger.info(f"📤 LOAD: Writing {len(df)} rows to PostgreSQL...")
    logger.info(f"   Target: {host}:{port}/{database}.public.{table_name}")
    start = time.time()

    # Ensure database exists
    ensure_database(host, port, user, password, database)

    # Build connection string
    pw_part = f":{password}" if password else ""
    conn_str = f"postgresql://{user}{pw_part}@{host}:{port}/{database}"
    engine = create_engine(conn_str)

    # Load in batches
    df.to_sql(
        name=table_name,
        con=engine,
        if_exists=if_exists,
        index=False,
        method="multi",
        chunksize=batch_size,
    )

    elapsed = time.time() - start
    logger.info(f"   ✅ Loaded {len(df)} rows in {elapsed:.2f}s")

    # Verify row count
    with engine.connect() as conn:
        result = conn.execute(text(f'SELECT COUNT(*) FROM "{table_name}"'))
        db_count = result.scalar()
        logger.info(f"   ✅ Verified: {db_count} rows in table '{table_name}'")

    # Show sample
    with engine.connect() as conn:
        result = conn.execute(text(f'SELECT * FROM "{table_name}" LIMIT 3'))
        sample = result.fetchall()
        logger.info(f"   📋 Sample rows:")
        for row in sample:
            logger.info(f"      {dict(row._mapping)}")

    return {
        "table": table_name,
        "database": database,
        "rows_loaded": len(df),
        "rows_verified": db_count,
        "elapsed_seconds": round(elapsed, 2),
    }


