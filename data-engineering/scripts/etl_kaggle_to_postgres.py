def run_quality_checks(df: pd.DataFrame) -> dict:
    """Run data quality validation before loading."""
    logger.info("🔍 QUALITY: Running pre-load quality checks...")

    checks = []

    # Row count
    row_status = "PASS" if len(df) > 0 else "FAIL"
    checks.append({"check": "row_count", "status": row_status, "value": len(df)})

    # Null analysis per column
    null_summary = df.isnull().sum()
    high_null_cols = null_summary[null_summary > len(df) * 0.5]
    null_status = "WARN" if len(high_null_cols) > 0 else "PASS"
    checks.append({
        "check": "null_analysis",
        "status": null_status,
        "high_null_columns": high_null_cols.to_dict() if len(high_null_cols) > 0 else {},
        "total_nulls": int(null_summary.sum()),
    })

    # Duplicate check
    dup_count = df.duplicated().sum()
    dup_status = "PASS" if dup_count == 0 else "WARN"
    checks.append({"check": "duplicates", "status": dup_status, "count": int(dup_count)})

    # Column type summary
    type_summary = df.dtypes.astype(str).value_counts().to_dict()
    checks.append({"check": "type_summary", "status": "INFO", "types": type_summary})

    # Print results
    for c in checks:
        icon = {"PASS": "✅", "FAIL": "❌", "WARN": "⚠️", "INFO": "ℹ️"}.get(c["status"], "❓")
        logger.info(f"   {icon} {c['check']}: {c['status']}")

    overall = "PASS" if all(c["status"] in ("PASS", "INFO") for c in checks) else "WARN"
    logger.info(f"   📊 Quality overall: {overall}")

    return {"overall": overall, "checks": checks}


