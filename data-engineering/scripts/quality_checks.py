def load_csv(filepath):
    """Load CSV and return rows + headers."""
    with open(filepath, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        rows = list(reader)
    return rows


def check_schema(rows, expected_columns):
    """Verify column names and order."""
    actual = list(rows[0].keys()) if rows else []
    missing = [c for c in expected_columns if c not in actual]
    extra = [c for c in actual if c not in expected_columns]
    return {
        "check": "schema_validation",
        "status": "PASS" if not missing and not extra else "FAIL",
        "expected_columns": len(expected_columns),
        "actual_columns": len(actual),
        "missing_columns": missing,
        "extra_columns": extra,
    }


def check_row_count(rows, min_rows=1, max_rows=1000000):
    """Verify row count is within expected range."""
    count = len(rows)
    return {
        "check": "row_count",
        "status": "PASS" if min_rows <= count <= max_rows else "FAIL",
        "row_count": count,
        "min_expected": min_rows,
        "max_expected": max_rows,
    }


def check_not_null(rows, columns):
    """Check for null/empty values in required columns."""
    results = []
    for col in columns:
        empty = [i+1 for i, r in enumerate(rows) if not r.get(col, "").strip()]
        results.append({
            "column": col,
            "status": "PASS" if not empty else "FAIL",
            "null_count": len(empty),
            "null_percentage": round(len(empty) / len(rows) * 100, 2),
            "sample_rows": empty[:5],
        })
    return {
        "check": "not_null",
        "status": "PASS" if all(r["status"] == "PASS" for r in results) else "FAIL",
        "details": results,
    }


def check_uniqueness(rows, columns):
    """Check for duplicate values in unique columns."""
    results = []
    for col in columns:
        values = [r.get(col, "") for r in rows]
        dupes = {v: c for v, c in Counter(values).items() if c > 1}
        results.append({
            "column": col,
            "status": "PASS" if not dupes else "FAIL",
            "duplicate_count": len(dupes),
            "duplicates": dict(list(dupes.items())[:5]),
        })
    return {
        "check": "uniqueness",
        "status": "PASS" if all(r["status"] == "PASS" for r in results) else "FAIL",
        "details": results,
    }


def check_email_format(rows):
    """Validate email format."""
    invalid = [(i+1, r["Email"]) for i, r in enumerate(rows)
               if not EMAIL_REGEX.match(r.get("Email", ""))]
    return {
        "check": "email_format",
        "status": "PASS" if not invalid else "FAIL",
        "invalid_count": len(invalid),
        "invalid_percentage": round(len(invalid) / len(rows) * 100, 2),
        "samples": invalid[:5],
    }


def check_url_format(rows):
    """Validate URL format."""
    invalid = [(i+1, r["Website"]) for i, r in enumerate(rows)
               if r.get("Website", "").strip() and not URL_REGEX.match(r["Website"])]
    return {
        "check": "url_format",
        "status": "PASS" if not invalid else "FAIL",
        "invalid_count": len(invalid),
        "samples": invalid[:5],
    }


def check_date_format(rows):
    """Validate date format (YYYY-MM-DD)."""
    invalid = []
    for i, r in enumerate(rows):
        d = r.get("Subscription Date", "")
        if not DATE_REGEX.match(d):
            invalid.append((i+1, d))
            continue
        try:
            datetime.strptime(d, "%Y-%m-%d")
        except ValueError:
            invalid.append((i+1, d))
    return {
        "check": "date_format",
        "status": "PASS" if not invalid else "FAIL",
        "invalid_count": len(invalid),
        "samples": invalid[:5],
    }


def check_phone_consistency(rows):
    """Analyze phone number format consistency."""
    formats = Counter()
    for r in rows:
        p = r.get("Phone 1", "")
        if p.startswith("+"):
            formats["international"] += 1
        elif p.startswith("001"):
            formats["001-prefix"] += 1
        elif "." in p:
            formats["dot-separated"] += 1
        elif "-" in p:
            formats["dash-separated"] += 1
        else:
            formats["plain"] += 1
    dominant = formats.most_common(1)[0] if formats else ("none", 0)
    consistency = round(dominant[1] / len(rows) * 100, 2) if rows else 0
    return {
        "check": "phone_consistency",
        "status": "PASS" if len(formats) <= 2 else "WARN",
        "formats_found": len(formats),
        "format_distribution": dict(formats),
        "dominant_format": dominant[0],
        "consistency_percentage": consistency,
    }


def check_customer_id_format(rows):
    """Validate Customer ID length and pattern."""
    invalid = [(i+1, r["Customer Id"]) for i, r in enumerate(rows)
               if len(r.get("Customer Id", "")) != CUSTOMER_ID_LENGTH]
    return {
        "check": "customer_id_format",
        "status": "PASS" if not invalid else "FAIL",
        "expected_length": CUSTOMER_ID_LENGTH,
        "invalid_count": len(invalid),
        "samples": invalid[:5],
    }


def check_freshness(rows):
    """Check data freshness based on Subscription Date."""
    dates = []
    for r in rows:
        try:
            dates.append(datetime.strptime(r["Subscription Date"], "%Y-%m-%d"))
        except (ValueError, KeyError):
            pass
    if not dates:
        return {"check": "freshness", "status": "FAIL", "reason": "no valid dates"}
    latest = max(dates)
    oldest = min(dates)
    return {
        "check": "freshness",
        "status": "INFO",
        "latest_record": latest.strftime("%Y-%m-%d"),
        "oldest_record": oldest.strftime("%Y-%m-%d"),
        "date_range_days": (latest - oldest).days,
    }


