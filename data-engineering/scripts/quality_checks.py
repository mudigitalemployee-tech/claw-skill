def main():
    filepath = sys.argv[1] if len(sys.argv) > 1 else INPUT_FILE
    print(f"🔍 Running data quality checks on: {filepath}")
    print("=" * 60)

    rows = load_csv(filepath)

    checks = [
        check_schema(rows, EXPECTED_COLUMNS),
        check_row_count(rows, min_rows=1, max_rows=1000000),
        check_not_null(rows, NOT_NULL_COLUMNS),
        check_uniqueness(rows, UNIQUE_COLUMNS),
        check_email_format(rows),
        check_url_format(rows),
        check_date_format(rows),
        check_phone_consistency(rows),
        check_customer_id_format(rows),
        check_freshness(rows),
    ]

    report = {
        "file": filepath,
        "row_count": len(rows),
        "column_count": len(rows[0].keys()) if rows else 0,
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "checks": checks,
    }

    # Print results
    for c in checks:
        icon = {"PASS": "✅", "FAIL": "❌", "WARN": "⚠️", "INFO": "ℹ️"}.get(c["status"], "❓")
        print(f"  {icon} {c['check'].replace('_',' ').title()}: {c['status']}")

    passed = sum(1 for c in checks if c["status"] == "PASS")
    total = len(checks)
    print(f"\n{'=' * 60}")
    print(f"📊 Overall: {passed}/{total} checks passed")

    # Generate HTML report
    today = datetime.now().strftime("%Y%m%d")
    filename = filepath.split("/")[-1].replace(".csv", "")
    html_path = f"{REPORT_DIR}/{filename}_quality_report_{today}.html"
    generate_html_report(report, html_path)
    print(f"📄 HTML Report saved: {html_path}")

    # Save JSON report
    json_path = f"{REPORT_DIR}/{filename}_quality_report_{today}.json"
    with open(json_path, 'w') as f:
        json.dump(report, f, indent=2, default=str)
    print(f"📋 JSON Report saved: {json_path}")

    return report


if __name__ == "__main__":
    main()
